student = {
    "name": "Alice",
    "age": 16,
    "grade": 10,
    "subjects": ["Math", "English", "Science"]
}

# Accessing values
print("Name:", student["name"])

# Modifying values
student["age"] = 17
print("Updated Age:", student["age"])

# Adding a new key-value pair
student["city"] = "New York"
print("Student Information:", student)
